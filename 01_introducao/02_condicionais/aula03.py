# Pegando a entrada do usuário
num1 = float(input("Digite um número: "))
num2 = float(input("Digite outro número: "))

# Operadores de comparação
print(f"Os números {num1} e {num2} são iguais: {num1 == num2}")
print(f"Os números {num1} e {num2} são diferentes: {num1 != num2}")
print(f"O número {num1} é maior que o número {num2}: {num1 > num2}")
print(f"O número {num1} é menor que o número {num2}: {num1 < num2}")
print(f"O número {num1} é maior ou igual o número {num2}: {num1 >= num2}")
print(f"O número {num1} é menor ou igual o número {num2}: {num1 <= num2}")
