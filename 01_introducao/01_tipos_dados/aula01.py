"""
# Tipos de variáveis
- string (str)
- número inteiro (int)
- número real (float)
- True or False (bool)
"""

# Declarando variáveis
nome = "Moisés"
idade = 23
nota = 9.50
passou = True

## Imprimindo as variáveis e seus tipos de dados
print(nome, type(nome))
print(idade, type(idade))
print(nota, type(nota))
print(passou, type(passou))
print()

## Operadores Aritméticos
print("Adição:", 5 + 5)
print("Subtração:", 10 - 5)
print("Divisão:", 10 / 2)
print("Divisão real:", 13 // 2)
print("Resto da divisão:", 15 / 4)
print("Potenciação:", 3 ** 3)
print()

## Concatenação de strings
print("Curso do " + "Senai Play")

# Multiplicação de caracteres
print(4 * "#-")